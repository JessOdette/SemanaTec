# Selecciona un tablero aleatoriamente al iniciar el juego
tiles = choice(boards)
# Función para obtener posiciones válidas en el tablero
def get_valid_position():
    valid_positions = []
    for index, tile in enumerate(tiles):
        if tile == 1:  # Solo las casillas "moradas"
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            valid_positions.append(vector(x, y))
    return choice(valid_positions)

# Posición inicial dinámica de Pacman y los fantasmas en zonas válidas
